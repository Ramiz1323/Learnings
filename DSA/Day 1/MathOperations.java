public class MathOperations {
    public static void main(String[] args) {
        System.out.println("Max: " + Math.max(10,17));
        System.out.println("Min: " + Math.min(10,17));
        System.out.println("Floor: " + (int)Math.floor(16.2));
        System.out.println("Ceiling: " + Math.ceil(16.2));
        System.out.println("Power: " + Math.pow(2,3));
        System.out.println("Square Root: " + Math.sqrt(16));
        System.out.println("Round: " + Math.round(18.6));
        System.out.println("Absolute: " + Math.abs(-5));
    }
}
